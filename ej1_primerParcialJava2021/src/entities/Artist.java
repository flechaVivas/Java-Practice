package entities;

public class Artist {

	private String name;
	private double hourlyCost;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public double getHourlyCost() {
		return hourlyCost;
	}
	public void setHourlyCost(double hourlyCost) {
		this.hourlyCost = hourlyCost;
	}
	
	
	
}
